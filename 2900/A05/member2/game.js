"use strict"; // Do NOT remove this directive!

PS.init = function( system, options ) {
	// Establish grid dimensions

	PS.gridSize( 12, 6 );

	// Set background color to Perlenspiel logo gray

	PS.gridColor( PS.COLOR_BLACK );

	// Change status line color and text

	PS.statusColor( PS.COLOR_WHITE );
	PS.statusText( "After every storm comes a rainbow" );

	// Preload click sound

	PS.audioLoad( "fx_bloop" );
};

PS.touch = function( x, y, data, options ) {

	// Variable to store the new assigned color
	let newColor;

	// Decided the new color depending on the y value
	// Each row is assigned a color, revealing a rainbow!
	if ( data !== PS.COLOR_WHITE ) {
		if ( y == 0 ) {
			newColor = PS.COLOR_RED;
		}
		if ( y == 1 ) {
			newColor = PS.COLOR_ORANGE;
		}
		if ( y == 2 ) {
			newColor = PS.COLOR_YELLOW;
		}
		if ( y == 3 ) {
			newColor = PS.COLOR_GREEN;
		}
		if ( y == 4 ) {
			newColor = PS.COLOR_BLUE;
		}
		if ( y == 5 ) {
			newColor = PS.COLOR_VIOLET;
		}
	}

	else {
		newColor = PS.COLOR_WHITE;
	}

	PS.color( x, y, newColor ); // set color to new assigned color

	// Decide what the next color should be.
	// If the current value was not white, change it to white.
	// Otherwise change it to one of the rainbow colors.

	let next; // variable to save next color

	if ( data !== PS.COLOR_WHITE ) {
		next = PS.COLOR_WHITE;
	}
	else {
		next = newColor;
	}


	// Remember the newly-changed color by storing it in the bead's data.

	PS.data( x, y, newColor );

	// Play click sound.

	PS.audioPlay( "fx_bloop" );
};

